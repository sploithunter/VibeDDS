pub fn type_information_for(type_name: &str) -> Option<&'static [u8]> {
    match type_name {
        HelloWorldType::TYPE_NAME => Some(&HELLOWORLD_TYPE_INFORMATION),
        ShapeType::TYPE_NAME => Some(&SHAPETYPE_TYPE_INFORMATION),
        PingType::TYPE_NAME => Some(&PINGTYPE_TYPE_INFORMATION),
        _ => None,
    }
}

fn sanitize_type_name_for_env(type_name: &str) -> String {
    type_name
        .chars()
        .map(|ch| if ch.is_ascii_alphanumeric() { ch } else { '_' })
        .collect::<String>()
        .to_uppercase()
}

fn parse_hex_bytes(value: &str) -> Option<Vec<u8>> {
    let mut cleaned = value.trim();
    if let Some(stripped) = cleaned.strip_prefix("0x") {
        cleaned = stripped;
    }
    let filtered: String = cleaned
        .chars()
        .filter(|ch| ch.is_ascii_hexdigit())
        .collect();
    if filtered.is_empty() {
        return None;
    }
    if filtered.len() % 2 != 0 {
        return None;
    }
    let mut bytes = Vec::with_capacity(filtered.len() / 2);
    let mut chars = filtered.chars();
    while let (Some(hi), Some(lo)) = (chars.next(), chars.next()) {
        let hex = [hi, lo].iter().collect::<String>();
        let val = u8::from_str_radix(&hex, 16).ok()?;
        bytes.push(val);
    }
    Some(bytes)
}

pub fn type_object_for(type_name: &str) -> Option<Vec<u8>> {
    if let Ok(val) = env::var("VIBEDDS_SEDP_TYPE_OBJECT_HEX") {
        if let Some(bytes) = parse_hex_bytes(&val) {
            return Some(bytes);
        }
    }
    let env_key = format!(
        "VIBEDDS_SEDP_TYPE_OBJECT_HEX_{}",
        sanitize_type_name_for_env(type_name)
    );
    if let Ok(val) = env::var(env_key) {
        if let Some(bytes) = parse_hex_bytes(&val) {
            return Some(bytes);
        }
    }
    match type_name {
        HelloWorldType::TYPE_NAME => Some(HELLOWORLD_TYPE_OBJECT.to_vec()),
        ShapeType::TYPE_NAME => Some(SHAPETYPE_TYPE_OBJECT.to_vec()),
        _ => None,
    }
}

pub fn type_is_keyed(type_name: &str) -> bool {
    if type_name == ShapeType::TYPE_NAME {
        return true;
    }
    if let Ok(val) = env::var("VIBEDDS_KEYED_TYPES") {
        for part in val.split(',') {
            if part.trim() == type_name {
                return true;
            }
        }
    }
    false
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_hello_world_roundtrip() {
        let message = "Hello, DDS World!";
        let bytes = HelloWorldType::serialize(message);
        let decoded = HelloWorldType::deserialize(&bytes).unwrap();
        assert_eq!(decoded, message);
    }

    #[test]
    fn test_hello_world_empty_string() {
        let bytes = HelloWorldType::serialize("");
        let decoded = HelloWorldType::deserialize(&bytes).unwrap();
        assert_eq!(decoded, "");
    }

    #[test]
    fn test_hello_world_utf8() {
        let message = "Hello, ‰∏ñÁïå! üåç";
        let bytes = HelloWorldType::serialize(message);
        let decoded = HelloWorldType::deserialize(&bytes).unwrap();
        assert_eq!(decoded, message);
    }

    #[test]
    fn test_hello_world_known_bytes() {
        // Known CDR encoding for "Hi"
        // Encapsulation: 00 01 00 00 (CDR_LE)
        // String length: 03 00 00 00 (3, including NUL)
        // String: "Hi\0"
        let bytes = HelloWorldType::serialize("Hi");

        // Check encapsulation header
        assert_eq!(&bytes[0..4], &[0x00, 0x01, 0x00, 0x00]);

        // Check string length (u32 LE = 3)
        assert_eq!(&bytes[4..8], &[0x03, 0x00, 0x00, 0x00]);

        // Check string content
        assert_eq!(&bytes[8..11], b"Hi\x00");
    }

    #[test]
    fn test_shape_type_roundtrip() {
        let shape = Shape {
            color: "BLUE".to_string(),
            x: 100,
            y: 200,
            shapesize: 30,
            fill_kind: 0,
            angle: 0,
        };

        let bytes = ShapeType::serialize_shape(&shape);
        let decoded = ShapeType::deserialize(&bytes).unwrap();

        assert_eq!(decoded, shape);
    }

    #[test]
    fn test_shape_type_known_bytes() {
        let bytes = ShapeType::serialize("RED", 50, 75, 25);

        // Check encapsulation header
        assert_eq!(&bytes[0..4], &[0x00, 0x01, 0x00, 0x00]);

        // Verify we can round-trip
        let decoded = ShapeType::deserialize(&bytes).unwrap();
        assert_eq!(decoded.color, "RED");
        assert_eq!(decoded.x, 50);
        assert_eq!(decoded.y, 75);
        assert_eq!(decoded.shapesize, 25);
        assert_eq!(decoded.fill_kind, 0);
        assert_eq!(decoded.angle, 0);
    }

    #[test]
    fn test_ping_type_roundtrip() {
        for count in [0, 1, 100, 1000000, -1, i32::MAX, i32::MIN] {
            let bytes = PingType::serialize(count);
            let decoded = PingType::deserialize(&bytes).unwrap();
            assert_eq!(decoded, count);
        }
    }

    #[test]
    fn test_ping_type_known_bytes() {
        // Ping with count=42
        let bytes = PingType::serialize(42);

        // Check encapsulation header
        assert_eq!(&bytes[0..4], &[0x00, 0x01, 0x00, 0x00]);

        // Check count (i32 LE = 42 = 0x2A)
        assert_eq!(&bytes[4..8], &[0x2A, 0x00, 0x00, 0x00]);
    }
}
